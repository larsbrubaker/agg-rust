use agg_rust::basics::{VertexSource, PATH_CMD_MOVE_TO, PATH_CMD_LINE_TO, PATH_CMD_STOP};
use agg_rust::path_storage::PathStorage;

/// Spiral vertex source matching the C++ spiral class in scanline_boolean2.cpp.
///
/// Generates an Archimedean spiral from inner radius r1 to outer radius r2.
pub struct Spiral {
    cx: f64,
    cy: f64,
    r1: f64,
    r2: f64,
    da: f64,
    dr: f64,
    start_angle: f64,
    angle: f64,
    curr_r: f64,
    start: bool,
}

impl Spiral {
    pub fn new(x: f64, y: f64, r1: f64, r2: f64, step: f64, start_angle: f64) -> Self {
        Self {
            cx: x,
            cy: y,
            r1,
            r2,
            da: (4.0_f64).to_radians(), // deg2rad(4.0) -- matches C++ exactly
            dr: step / 90.0,            // matches C++ exactly
            start_angle,
            angle: start_angle,
            curr_r: r1,
            start: true,
        }
    }
}

impl VertexSource for Spiral {
    fn rewind(&mut self, _path_id: u32) {
        self.angle = self.start_angle;
        self.curr_r = self.r1;
        self.start = true;
    }

    fn vertex(&mut self, x: &mut f64, y: &mut f64) -> u32 {
        if self.curr_r > self.r2 {
            return PATH_CMD_STOP;
        }
        *x = self.cx + self.angle.cos() * self.curr_r;
        *y = self.cy + self.angle.sin() * self.curr_r;
        self.curr_r += self.dr;
        self.angle += self.da;
        if self.start {
            self.start = false;
            return PATH_CMD_MOVE_TO;
        }
        PATH_CMD_LINE_TO
    }
}

