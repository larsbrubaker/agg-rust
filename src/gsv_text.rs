#[derive(Clone, Copy, PartialEq)]
enum Status {
    Initial,
    NextChar,
    StartGlyph,
    Glyph,
}

/// Read a little-endian u16 from a byte slice at the given offset.
#[inline]
fn value_le(data: &[u8], offset: usize) -> u16 {
    u16::from_le_bytes([data[offset], data[offset + 1]])
}

/// Geometric stroke vector text renderer.
///
/// Renders text using a built-in stroke-based font. Each character is
/// described as a series of stroked line segments, making the output
/// resolution-independent.
///
/// Port of C++ `gsv_text`.
pub struct GsvText {
    x: f64,
    y: f64,
    start_x: f64,
    width: f64,
    height: f64,
    space: f64,
    line_space: f64,
    text: Vec<u8>,
    cur_chr: usize,
    font: &'static [u8],
    loaded_font: Vec<u8>,
    status: Status,
    flip: bool,
    // Offsets into the font data
    indices: usize,
    glyphs: usize,
    bglyph: usize,
    eglyph: usize,
    // Computed scale factors
    w: f64,
    h: f64,
}

impl GsvText {
    pub fn new() -> Self {
        Self {
            x: 0.0,
            y: 0.0,
            start_x: 0.0,
            width: 10.0,
            height: 0.0,
            space: 0.0,
            line_space: 0.0,
            text: Vec::new(),
            cur_chr: 0,
            font: GSV_DEFAULT_FONT,
            loaded_font: Vec::new(),
            status: Status::Initial,
            flip: false,
            indices: 0,
            glyphs: 0,
            bglyph: 0,
            eglyph: 0,
            w: 0.0,
            h: 0.0,
        }
    }

    /// Set the font data. If `None`, uses the loaded font (if any).
    pub fn set_font(&mut self, font: Option<&'static [u8]>) {
        match font {
            Some(f) => self.font = f,
            None => {
                if !self.loaded_font.is_empty() {
                    // Can't assign loaded_font as &'static, so we keep using
                    // the default font. C++ version uses a raw pointer here.
                    // In Rust we handle this differently in load_font.
                }
            }
        }
    }

    pub fn flip(&mut self, flip_y: bool) {
        self.flip = flip_y;
    }

    pub fn size(&mut self, height: f64, width: f64) {
        self.height = height;
        self.width = width;
    }

    pub fn space(&mut self, space: f64) {
        self.space = space;
    }

    pub fn line_space(&mut self, line_space: f64) {
        self.line_space = line_space;
    }

    pub fn start_point(&mut self, x: f64, y: f64) {
        self.x = x;
        self.start_x = x;
        self.y = y;
    }

    pub fn text(&mut self, text: &str) {
        self.text = text.as_bytes().to_vec();
    }

    /// Load font data from a byte slice (replaces C++ file-based load_font).
    pub fn load_font_data(&mut self, data: &[u8]) {
        self.loaded_font = data.to_vec();
    }

    /// Get the active font data. Uses loaded_font if available, else default.
    fn active_font(&self) -> &[u8] {
        if !self.loaded_font.is_empty() {
            &self.loaded_font
        } else {
            self.font
        }
    }

    /// Compute the bounding width of the current text.
    pub fn text_width(&mut self) -> f64 {
        match bounding_rect_single(self, 0) {
            Some(r) => r.x2 - r.x1,
            None => 0.0,
        }
    }
}

impl Default for GsvText {
    fn default() -> Self {
        Self::new()
    }
}

impl VertexSource for GsvText {
    fn rewind(&mut self, _path_id: u32) {
        self.status = Status::Initial;
        let font = self.active_font();
        if font.is_empty() {
            return;
        }

        let base_height = value_le(font, 4) as f64;
        let idx_offset = value_le(font, 0) as usize;
        self.indices = idx_offset;
        self.glyphs = idx_offset + 257 * 2;
        self.h = self.height / base_height;
        self.w = if self.width == 0.0 {
            self.h
        } else {
            self.width / base_height
        };
        if self.flip {
            self.h = -self.h;
        }
        self.cur_chr = 0;
    }

    fn vertex(&mut self, x: &mut f64, y: &mut f64) -> u32 {
        // C++ uses switch with fallthrough: initial → next_char → start_glyph → glyph.
        // Font data is accessed via short-lived borrows to avoid conflicting
        // with mutations of self.status, self.cur_chr, etc.
        loop {
            match self.status {
                Status::Initial => {
                    if self.active_font().is_empty() {
                        return PATH_CMD_STOP;
                    }
                    self.status = Status::NextChar;
                    continue;
                }
                Status::NextChar => {
                    if self.cur_chr >= self.text.len() {
                        return PATH_CMD_STOP;
                    }
                    let idx = self.text[self.cur_chr] as usize;
                    self.cur_chr += 1;
                    if idx == b'\n' as usize {
                        self.x = self.start_x;
                        self.y -= if self.flip {
                            -self.height - self.line_space
                        } else {
                            self.height + self.line_space
                        };
                        continue;
                    }
                    let idx2 = idx << 1;
                    // Read glyph offsets from font with short-lived borrow
                    let bg = value_le(self.active_font(), self.indices + idx2) as usize;
                    let eg = value_le(self.active_font(), self.indices + idx2 + 2) as usize;
                    self.bglyph = self.glyphs + bg;
                    self.eglyph = self.glyphs + eg;
                    self.status = Status::StartGlyph;
                    // fallthrough to StartGlyph
                    continue;
                }
                Status::StartGlyph => {
                    *x = self.x;
                    *y = self.y;
                    self.status = Status::Glyph;
                    return PATH_CMD_MOVE_TO;
                }
                Status::Glyph => {
                    if self.bglyph >= self.eglyph {
                        self.status = Status::NextChar;
                        self.x += self.space;
                        continue;
                    }
                    // Read glyph bytes with short-lived borrow
                    let (dx_byte, yc_raw) = {
                        let f = self.active_font();
                        (f[self.bglyph], f[self.bglyph + 1])
                    };
                    self.bglyph += 2;

                    let dx = dx_byte as i8;
                    let yf = yc_raw & 0x80;
                    // Sign-extend the lower 7 bits: shift left 1 then
                    // arithmetic shift right 1 (matching C++ `yc <<= 1; yc >>= 1`)
                    let yc = ((yc_raw as i8) << 1) >> 1;
                    let dy = yc as i32;

                    self.x += dx as f64 * self.w;
                    self.y += dy as f64 * self.h;
                    *x = self.x;
                    *y = self.y;
                    return if yf != 0 {
                        PATH_CMD_MOVE_TO
                    } else {
                        PATH_CMD_LINE_TO
                    };
                }
            }
        }
    }
}

